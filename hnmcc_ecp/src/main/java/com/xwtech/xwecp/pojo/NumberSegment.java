package com.xwtech.xwecp.pojo;

public class NumberSegment {

	private int netId;
	
	private int city;
	
	private int numSegment;

	public int getNetId() {
		return netId;
	}

	public void setNetId(int netId) {
		this.netId = netId;
	}

	public int getCity() {
		return city;
	}

	public void setCity(int city) {
		this.city = city;
	}

	public int getNumSegment() {
		return numSegment;
	}

	public void setNumSegment(int numSegment) {
		this.numSegment = numSegment;
	}
}
