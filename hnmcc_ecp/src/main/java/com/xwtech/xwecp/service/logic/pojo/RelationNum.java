package com.xwtech.xwecp.service.logic.pojo;


public class RelationNum
{
	/**
	 * 号码类型
	 */
	private String phoneType;
	/**
	 * 亲情/情侣号码
	 */
	private String phoneNum;
	/**
	 * 业务流水号
	 */
	private String bussNo;
	/**
	 * 开始日期
	 */
	private String beginDate;
	/**
	 * 结束日期
	 */
	private String endDate;

	private int mainCard;
	/**
	 * 订购类型
	 */
	private String offerDate;

	private int flag;

	public String getPhoneType() {
		return phoneType;
	}

	public void setPhoneType(String phoneType) {
		this.phoneType = phoneType;
	}

	public String getPhoneNum() {
		return phoneNum;
	}

	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}

	public String getBussNo() {
		return bussNo;
	}

	public void setBussNo(String bussNo) {
		this.bussNo = bussNo;
	}

	public String getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(String beginDate) {
		this.beginDate = beginDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public int getMainCard() {
		return mainCard;
	}

	public void setMainCard(int mainCard) {
		this.mainCard = mainCard;
	}

	public String getOfferDate() {
		return offerDate;
	}

	public void setOfferDate(String offerDate) {
		this.offerDate = offerDate;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	@Override
	public String toString() {
		return "RelationNum [phoneType=" + phoneType + ", phoneNum=" + phoneNum
				+ ", bussNo=" + bussNo + ", beginDate=" + beginDate
				+ ", endDate=" + endDate + ", mainCard=" + mainCard
				+ ", offerDate=" + offerDate + ", flag=" + flag + "]";
	}

	
}