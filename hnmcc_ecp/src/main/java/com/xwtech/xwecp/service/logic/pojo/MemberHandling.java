package com.xwtech.xwecp.service.logic.pojo;

/**
 * 根据外围渠道订单流水查询运营商订单处理情况
 */
public class MemberHandling {

    private String billId;//手机号码
    private String memSrvpkgDesc;//订购资费
    private String validDate;//生效时间
    private String state;//处理结果
    private String dealResult;//处理结果

    public String getBillId() {
        return billId;
    }

    public void setBillId(String billId) {
        this.billId = billId;
    }

    public String getMemSrvpkgDesc() {
        return memSrvpkgDesc;
    }

    public void setMemSrvpkgDesc(String memSrvpkgDesc) {
        this.memSrvpkgDesc = memSrvpkgDesc;
    }

    public String getValidDate() {
        return validDate;
    }

    public void setValidDate(String validDate) {
        this.validDate = validDate;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getDealResult() {
        return dealResult;
    }

    public void setDealResult(String dealResult) {
        this.dealResult = dealResult;
    }

    @Override
    public String toString() {
        return "MemberHandling{" +
                "billId='" + billId + '\'' +
                ", memSrvpkgDesc='" + memSrvpkgDesc + '\'' +
                ", validDate='" + validDate + '\'' +
                ", state='" + state + '\'' +
                ", dealResult='" + dealResult + '\'' +
                '}';
    }
}
