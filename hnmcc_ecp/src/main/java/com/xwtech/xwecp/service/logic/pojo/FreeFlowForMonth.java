package com.xwtech.xwecp.service.logic.pojo;

public class FreeFlowForMonth {
	private String itemId;
	private String itemName;
	private String unitDes;
	private String freeRes;
	private String freeResUsed;
	private String freeResLeft;
	private String validDate;
	private String expireDate;
	private String freeResType;
	
	public String getItemId() {
		return itemId;
	}
	public void setItemId(String itemId) {
		this.itemId = itemId;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getUnitDes() {
		return unitDes;
	}
	public void setUnitDes(String unitDes) {
		this.unitDes = unitDes;
	}
	public String getFreeRes() {
		return freeRes;
	}
	public void setFreeRes(String freeRes) {
		this.freeRes = freeRes;
	}
	public String getFreeResUsed() {
		return freeResUsed;
	}
	public void setFreeResUsed(String freeResUsed) {
		this.freeResUsed = freeResUsed;
	}
	public String getFreeResLeft() {
		return freeResLeft;
	}
	public void setFreeResLeft(String freeResLeft) {
		this.freeResLeft = freeResLeft;
	}
	public String getValidDate() {
		return validDate;
	}
	public void setValidDate(String validDate) {
		this.validDate = validDate;
	}
	public String getExpireDate() {
		return expireDate;
	}
	public void setExpireDate(String expireDate) {
		this.expireDate = expireDate;
	}
	public String getFreeResType() {
		return freeResType;
	}
	public void setFreeResType(String freeResType) {
		this.freeResType = freeResType;
	}
	
}
