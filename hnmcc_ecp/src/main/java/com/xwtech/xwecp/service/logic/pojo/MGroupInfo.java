package com.xwtech.xwecp.service.logic.pojo;

public class MGroupInfo {
	private String phoneNum;// 群组号码
	private String prodEffDate;//生效时间
	private String prodExpDate;//失效时间
	public String getPhoneNum() {
		return phoneNum;
	}
	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}
	public String getProdEffDate() {
		return prodEffDate;
	}
	public void setProdEffDate(String prodEffDate) {
		this.prodEffDate = prodEffDate;
	}
	public String getProdExpDate() {
		return prodExpDate;
	}
	public void setProdExpDate(String prodExpDate) {
		this.prodExpDate = prodExpDate;
	}
	
	@Override
	public String toString() {
		return "MGroupInfo [phoneNum=" + phoneNum + ", prodEffDate="
				+ prodEffDate + ", prodExpDate=" + prodExpDate + "]";
	}
	
	
}