package com.xwtech.xwecp.service.logic.pojo;

public class TransfernetCarryUser {

    private String retCode;
    private String errMsg;
    private String isHNMobile;
    private String numState;
    private String npState;
    private String homeNet;

    public String getRetCode() {
        return retCode;
    }

    public void setRetCode(String retCode) {
        this.retCode = retCode;
    }

    public String getErrMsg() {
        return errMsg;
    }

    public void setErrMsg(String errMsg) {
        this.errMsg = errMsg;
    }

    public String getIsHNMobile() {
        return isHNMobile;
    }

    public void setIsHNMobile(String isHNMobile) {
        this.isHNMobile = isHNMobile;
    }

    public String getNumState() {
        return numState;
    }

    public void setNumState(String numState) {
        this.numState = numState;
    }

    public String getNpState() {
        return npState;
    }

    public void setNpState(String npState) {
        this.npState = npState;
    }

    public String getHomeNet() {
        return homeNet;
    }

    public void setHomeNet(String homeNet) {
        this.homeNet = homeNet;
    }

    @Override
    public String toString() {
        return "TransfernetCarryUser{" +
                "retCode='" + retCode + '\'' +
                ", errMsg='" + errMsg + '\'' +
                ", isHNMobile='" + isHNMobile + '\'' +
                ", numState='" + numState + '\'' +
                ", npState='" + npState + '\'' +
                ", homeNet='" + homeNet + '\'' +
                '}';
    }
}
