package com.xwtech.xwecp.service.logic.pojo;

/**
 * 用户信用星级评定明细查询  - 单项得分信息
 * @author YangXQ
 * 2014-12-12
 */
public class Detail {
	private String factor_id;
	private String user_value;
	private long   cal_result;
	public String getFactor_id() {
		return factor_id;
	}
	public void setFactor_id(String factorId) {
		factor_id = factorId;
	}
	public String getUser_value() {
		return user_value;
	}
	public void setUser_value(String userValue) {
		user_value = userValue;
	}
	public long getCal_result() {
		return cal_result;
	}
	public void setCal_result(long calResult) {
		cal_result = calResult;
	}

	
}
